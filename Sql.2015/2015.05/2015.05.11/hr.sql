SELECT
  E.EMPLOYEE_ID,
  E.FIRST_NAME,
  D.DEPARTMENT_NAME
FROM
  EMPLOYEES E,
  DEPARTMENTS D
WHERE
  E.DEPARTMENT_ID    = D.DEPARTMENT_ID(+)
AND E.JOB_ID         = 'SH_CLERK'
AND D.LOCATION_ID(+) = '1500';

SELECT
  E.EMPLOYEE_ID,
  E.FIRST_NAME,
  D.DEPARTMENT_NAME,
  L.CITY
FROM
  EMPLOYEES E,
  DEPARTMENTS D,
  LOCATIONS L
WHERE
  E.DEPARTMENT_ID = D.DEPARTMENT_ID
AND
  D.LOCATION_ID = L.LOCATION_ID
AND 
  E.JOB_ID = 'FI_ACCOUNT';


SELECT
  E.EMPLOYEE_ID,
  E.FIRST_NAME,
  D.DEPARTMENT_NAME,
  L.CITY
FROM
  EMPLOYEES E,
  DEPARTMENTS D,
  LOCATIONS L
WHERE
  E.DEPARTMENT_ID(+) = D.DEPARTMENT_ID
AND D.LOCATION_ID = L.LOCATION_ID
ORDER BY
  D.DEPARTMENT_ID ASC;
  

