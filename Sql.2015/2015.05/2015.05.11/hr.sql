SELECT JOB_ID ,
  SUM(SALARY)
FROM EMPLOYEES
WHERE JOB_ID LIKE '%_REP'
ORDER BY SUM(SALARY);

SELECT
  JOB_ID,
  SUM(SALARY)
FROM
  EMPLOYEES
WHERE
  JOB_ID NOT LIKE '%_REP'
GROUP BY
  JOB_ID
HAVING
  SUM(SALARY)> 13000
ORDER BY
  SUM(SALARY);
  
