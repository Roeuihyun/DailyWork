package example02;

/**
 * 
 * 정수의 사칙연산
 *
 */
public class NumberEx {
	
	public static void main(String[] args){
		int num1 = 10;
		int num2 = 3;
		
		int num3 = num1 + num2;
		int num4 = num1 - num2;
		int num5 = num1 * num2;
		int num6 = num1 / num2;
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(num5);
		System.out.println(num6);
	}

}
